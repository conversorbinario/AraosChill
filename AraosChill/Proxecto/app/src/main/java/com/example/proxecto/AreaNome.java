package com.example.proxecto;

public class AreaNome {

    private String area;
    private String nome;

    public AreaNome(String area, String nome) {
        this.area = area;
        this.nome = nome;
    }

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
}
